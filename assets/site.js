const pageTitles = {
  home: "homeTitle",
  support: "supportTitle",
  privacy: "privacyTitle"
};

const buttons = document.querySelectorAll("[data-lang-button]");

function preferredLanguage() {
  const saved = readSavedLanguage();
  if (saved && dictionaries[saved]) return saved;
  const browser = (navigator.language || "pt-BR").toLowerCase();
  if (browser.startsWith("en")) return "en";
  if (browser.startsWith("es")) return "es";
  return "pt-BR";
}

function readSavedLanguage() {
  try {
    return localStorage.getItem("alphax-language");
  } catch (error) {
    return null;
  }
}

function saveLanguage(lang) {
  try {
    localStorage.setItem("alphax-language", lang);
  } catch (error) {
    return;
  }
}

function renderList(node, items) {
  node.innerHTML = "";
  items.forEach((item) => {
    const li = document.createElement("li");
    li.textContent = item;
    node.appendChild(li);
  });
}

function setLanguage(lang) {
  const copy = dictionaries[lang] || dictionaries["pt-BR"];
  const page = document.body.dataset.page || "home";
  document.documentElement.lang = lang;
  document.title = copy[pageTitles[page]] || copy.homeTitle;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = copy[node.dataset.i18n] || "";
  });

  document.querySelectorAll("[data-i18n-list]").forEach((node) => {
    renderList(node, copy[node.dataset.i18nList] || []);
  });

  buttons.forEach((button) => {
    button.setAttribute("aria-pressed", String(button.dataset.langButton === lang));
  });

  saveLanguage(lang);
}

buttons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.langButton));
});

setLanguage(preferredLanguage());
